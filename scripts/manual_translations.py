import json

# Manual translation of all new keys into 6 languages.
# Rules: NO em dashes. NO "is not X - but Y" phrasing. Hyphens only.
# Proper nouns (names, places, party names) stay as-is or get natural adaptation.
# HTML markup is preserved identically.

# Keys that should NOT be translated (proper names, HTML images, emoji, numbers, dates, locations)
PASSTHROUGH_KEYS = set()

# Load source
with open('data/new_source.json', 'r', encoding='utf-8') as f:
    source = json.load(f)

# Identify passthrough keys automatically
for k, v in source.items():
    # Skip images/logos
    if '<img ' in v:
        PASSTHROUGH_KEYS.add(k)
    # Skip emoji
    elif v.strip() in ['🌗']:
        PASSTHROUGH_KEYS.add(k)
    # Skip pure numbers
    elif v.strip().isdigit():
        PASSTHROUGH_KEYS.add(k)
    # Skip month abbreviations (short, contextual - keep same)
    elif v.strip() in ['Aug', 'Sep', 'Oct', 'Nov', 'Dec', 'Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul']:
        PASSTHROUGH_KEYS.add(k)
    # Skip language names in the dropdown (they stay as their fictional names)
    elif v.strip() in ['Alanian', 'Gallic', 'Alcamerian', 'Rälandic', 'Myrati', 'Austrumish', 'Leislandic']:
        PASSTHROUGH_KEYS.add(k)
    # Skip proper names that are standalone
    elif k.startswith('auto-candidates-') and v.strip() in [
        'Mandy Trottier', 'Isolde von Dittarsdorf', 'Safiyya Bethune', 'Marcus Horvat',
        'Aldric von Reichel', 'William Smith', 'Sherwin Hildebrand', 'Adam Scholz',
        'Noodles dos Açores', 'Florence Marin', 'Valentine Magres', 'Stephen Bratanovic',
        'Brezhnev Prigozhn', 'Dorothy Ardern Roosevelt'
    ]:
        PASSTHROUGH_KEYS.add(k)
    # Skip name+logo combos
    elif 'Mandy Trottier' in v and '<img' in v:
        PASSTHROUGH_KEYS.add(k)
    # Skip standalone place names
    elif v.strip() in ['National', 'Ambrosia', 'the Republic', 'Kazana', 'Mezata', 'Oplana', 'Reno', 'State/Regional']:
        PASSTHROUGH_KEYS.add(k)
    # Skip RSVP buttons with HTML
    elif v.strip().startswith('<a class='):
        PASSTHROUGH_KEYS.add(k)

print(f"Passthrough keys (no translation needed): {len(PASSTHROUGH_KEYS)}")

